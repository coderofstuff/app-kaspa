static void test_invalid_type(void **state) {
    // Even Y-coord test case
    uint8_t public_key[1] = {0};

    uint8_t address[1] = {0};

    bool res = address_from_pubkey(public_key, -1, address, 1);

    assert_int_equal(res, 0);
}

